def draw_tarot(birth_date: date, num_cards: int = 3) -> list[dict]:
    """
    สุ่มไพ่ทาโรต์จากวันเกิด
    num_cards: จำนวนไพ่ที่จั่ว (default 3 = อดีต ปัจจุบัน อนาคต)
    """
    seed = int(birth_date.strftime("%Y%m%d")) + date.today().toordinal()
    rng = random.Random(seed)
    
    cards = rng.sample(MAJOR_ARCANA, num_cards)
    results = []
    positions = ["อดีต 🕰️", "ปัจจุบัน 📍", "อนาคต 🔮", "สิ่งที่ควรรู้ 💡", "ผลลัพธ์ 🎯"]
    
    for i, card in enumerate(cards):
        is_reversed = rng.random() < 0.3  # 30% chance กลับหัว
        position = positions[i] if i < len(positions) else f"ใบที่ {i+1}"
        
        results.append({
            "card": card,
            "reversed": is_reversed,
            "position": position,
        })
    
    return results


def format_tarot(cards: list[dict]) -> str:
    """จัดรูปแบบผลไพ่ทาโรต์"""
    lines = [
        "🃏 ผลเปิดไพ่ทาโรต์",
        "━━━━━━━━━━━━━━━━━━━━",
    ]
    
    for item in cards:
        card = item["card"]
        is_rev = item["reversed"]
        pos = item["position"]
        
        direction = "🔙 กลับหัว" if is_rev else "✨ ตั้งตรง"
        meanings = card["reversed"] if is_rev else card["upright"]
        
        lines.append(f"\n{pos}")
        lines.append(f"{card['emoji']} {card['name']}")
        lines.append(f"   ({card['name_th']}) — {direction}")
        lines.append(f"   💫 {card['meaning']}")
        lines.append(f"   💕 รัก: {meanings['love']}")
        lines.append(f"   💼 งาน: {meanings['career']}")
        lines.append(f"   💰 เงิน: {meanings['money']}")
        lines.append(f"   🏥 สุขภาพ: {meanings['health']}")
    
    lines.append("\n━━━━━━━━━━━━━━━━━━━━")
    lines.append("⚠️ เป็นความเชื่อส่วนบุคคล โปรดใช้วิจารณญาณ 🙏")
    
    return "\n".join(lines)


def get_tarot_reading(birth_date: date) -> str:
    """ดูดวงไพ่ทาโรต์ — จั่ว 3 ใบ (อดีต ปัจจุบัน อนาคต)"""
    cards = draw_tarot(birth_date, 3)
    return format_tarot(cards)
