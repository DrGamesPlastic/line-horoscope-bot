import random
from datetime import date

def get_daily_horoscope(birth_date):
    today = date.today()
    # ใช้ "วันเกิด + วันนี้" เป็น Seed เพื่อให้ดวงเปลี่ยนทุกวัน แต่กดดูซ้ำในวันเดิมจะได้ผลเดิม
    seed_str = f"{birth_date.strftime('%Y%m%d')}-{today.strftime('%Y%m%d')}"
    random.seed(seed_str)

    # รายการคำทำนาย (ยิ่งใส่เยอะ ยิ่งดูหลากหลายครับ)
    predictions = [
        "🌟 วันนี้ดวงการงานพุ่งแรง มีเกณฑ์ได้รับข่าวดีหรือโปรเจกต์ใหม่ที่ท้าทาย",
        "💰 โชคลาภกำลังเดินทางมาหาคุณ มีเกณฑ์ได้ลาภลอยจากการเดินทางหรือคนใกล้ตัว",
        "🏥 สุขภาพ: ระวังอาการปวดเมื่อยกล้ามเนื้อ หรือความเครียดจากการทำงานสะสม",
        "💖 ความรัก: คนโสดมีเกณฑ์พบมิตรภาพใหม่ที่น่าสนใจ คนมีคู่ช่วงนี้บรรยากาศหวานชื่น",
        "🌈 วันนี้สีมงคลของคุณคือสีเขียวและสีทอง แนะนำให้สวมใส่เพื่อดึงดูดพลังงานบวก",
        "⚠️ คำเตือน: ระวังการสื่อสารที่คลาดเคลื่อนกับเพื่อนร่วมงาน ควรตรวจสอบข้อมูลให้ดี",
        "💎 วันนี้เป็นวันดีที่จะเริ่มทำสิ่งใหม่ๆ หรือวางแผนลงทุนในระยะยาว",
        "🍵 แนะนำให้หาเวลาพักผ่อน จิบน้ำชาหรือฟังเพลงเบาๆ เพื่อปรับสมดุลอารมณ์",
        "🍀 วันนี้จะมีนิมิตหมายที่ดีเกี่ยวกับเรื่องที่อยู่อาศัยหรือครอบครัว",
        "⚡️ ระวังเรื่องการใช้จ่ายฟุ่มเฟือยเกินจำเป็นในวันนี้"
    ]

    result = random.choice(predictions)
    
    header = f"🔮 ดวงรายวันประจำวันที่ {today.strftime('%d/%m/%Y')}\n"
    footer = "\n\n✨ ดวงรายวันนี้เปลี่ยนทุกวัน อย่าลืมกลับมาเช็กใหม่พรุ่งนี้นะครับ!"
    
    return header + "━━━━━━━━━━━━━━━━━━━━\n" + result + footer
