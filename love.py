def get_zodiac_name(birth_date: date) -> str:
    """หาราศีจากวันเกิด คืนชื่อสั้น"""
    from horoscope import get_thai_zodiac
    zodiac = get_thai_zodiac(birth_date)
    return zodiac["name_th"]


def get_love_compatibility(zodiac1: str, zodiac2: str) -> tuple[int, str]:
    """คำนวณสมพงศ์ระหว่าง 2 ราศี คืนเป็น (score, level)"""
    if zodiac1 not in ZODIAC_COMPAT or zodiac2 not in ZODIAC_COMPAT:
        return 50, "กลาง"
    
    data = ZODIAC_COMPAT[zodiac1]
    
    if zodiac2 in data["คู่ธาตุ"]:
        return random.randint(85, 100), "สูง"
    elif zodiac2 in data["คู่ดี"]:
        return random.randint(70, 89), "ดี"
    elif zodiac2 in data["คู่กลาง"]:
        return random.randint(50, 69), "กลาง"
    else:
        return random.randint(30, 55), "ท้าทาย"


